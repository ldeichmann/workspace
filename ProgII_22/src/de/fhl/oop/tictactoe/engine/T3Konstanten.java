package de.fhl.oop.tictactoe.engine;

/**
 * Statische Klasse zur Definition aller Konstanten, die im Spiel
 * Tic Tac Toe eingefuehrt werden.
 * @author Nane Kratzke
 *
 */
public class T3Konstanten {

	/**
	 * Zeichen fuer die Spielrolle X (Spiele 1).
	 */
	public final static char X = 'X';
	
	/**
	 * Zeichen fuer die Spielrolle O (Spieler 2).
	 */
	public final static char O = 'O';
	
	/**
	 * Zeichen fuer ein leeres Feld.
	 */
	public final static char LEER = ' ';
	
	/**
	 * Spielfeldbreite und (-hoehe).
	 */
	public final static int BREITE = 3;
	
	/**
	 * Horizontaler Seperater (genutzt bei der Konsolen-Ausgabe von Spielfeldern)
	 */
	public final static String HSEP = "|";
	
	/**
	 * Vertikaler Seperater (genutzt bei der Konsolen-Ausgabe von Spielfeldern)
	 */
	public final static String VSEP = "-+-+-";

}
